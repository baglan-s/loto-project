    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $setting = Setting::find($id);
        return view('admin.setting.edit', ['setting' => $setting]);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $setting = Setting::find($id);
        if ($file = $request->file('image')) {
            $file_path = $this->image_dir . $file->getClientOriginalName();
            $file->move($this->image_dir, $file->getClientOriginalName());

            $setting->main_image = $file_path;
        }

        if ($setting->save()) {
            $request->session()->flash('msg_success', 'Настройки успешно изменены!');
        }
        else {
            $request->session()->flash('msg_error', 'Ошибка! Попробуйте позже.');
        }

        return redirect(route('setting.edit', $id));
    }

